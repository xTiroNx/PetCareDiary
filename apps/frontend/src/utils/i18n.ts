export type TranslationKey = keyof typeof dictionaries.en;

function isLanguage(value: string | null | undefined): value is LanguageCode {
  return Boolean(value && languages.some((language) => language.code === value));
}

function detectLanguage(): LanguageCode {
  const saved = localStorage.getItem(storageKey);
  if (isLanguage(saved)) return saved;

  const browserLanguage = navigator.language.split("-")[0];
  if (isLanguage(browserLanguage)) return browserLanguage;
  return "en";
}

type I18nState = {
  language: LanguageCode;
  setLanguage: (language: LanguageCode) => void;
};

export const useI18nStore = create<I18nState>((set) => ({
  language: detectLanguage(),
  setLanguage: (language) => {
    localStorage.setItem(storageKey, language);
    document.documentElement.lang = language;
    set({ language });
  }
}));

export function useI18n() {
  const language = useI18nStore((state) => state.language);
  const setLanguage = useI18nStore((state) => state.setLanguage);

  function t(key: TranslationKey, params: Record<string, string | number> = {}) {
    let value: string = dictionaries[language][key] ?? dictionaries.en[key];
    Object.entries(params).forEach(([param, replacement]) => {
      value = value.replaceAll(`{{${param}}}`, String(replacement));
    });
    return value;
  }

  return { language, setLanguage, t };
}

export function languageLocale(language: LanguageCode) {
  return language === "zh" ? "zh-CN" : language;
}
